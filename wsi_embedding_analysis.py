class DataLoader:
    """Utility class for loading embeddings and clinical data."""

    def __init__(self, config: Config):
        self.config = config
        self.clinical_data = None
        self.slide_metadata = None

    def load_clinical_data(self) -> pd.DataFrame:
        """Load and filter clinical data for the specified cancer type."""
        logger.info(f"Loading clinical data from {self.config.clinical_data_path}")

        if not self.config.clinical_data_path.exists():
            raise FileNotFoundError(f"Clinical data not found: {self.config.clinical_data_path}")

        clinical = pd.read_parquet(self.config.clinical_data_path)

        # If participant_id is the index, make it a column
        if clinical.index.name == 'participant_id':
            clinical = clinical.reset_index()

        # Filter for cancer type
        if 'project_id' in clinical.columns:
            clinical = clinical[clinical['project_id'] == f"TCGA-{self.config.cancer_type}"]
        elif 'cancer_type' in clinical.columns:
            clinical = clinical[clinical['cancer_type'] == self.config.cancer_type]

        logger.info(f"Loaded {len(clinical)} samples for TCGA-{self.config.cancer_type}")

        self.clinical_data = clinical
        return clinical

    def load_slide_embeddings(self, slide_path: Path) -> np.ndarray:
        """Load embeddings from a single H5 file using memory-mapped reads.

        Opens the file with driver='core' and backing_store=False to avoid
        keeping the entire dataset resident.  For very large datasets the
        arrays are read in chunks so the OS can page memory efficiently.
        """
        with h5py.File(slide_path, 'r', rdcc_nbytes=0) as f:
            # Common key names for embeddings
            dataset = None
            for key in ['features', 'embeddings', 'feats', 'data']:
                if key in f:
                    dataset = f[key]
                    break
            if dataset is None:
                for key in f.keys():
                    if isinstance(f[key], h5py.Dataset):
                        dataset = f[key]
                        break

            if dataset is None:
                raise ValueError(f"Could not find embeddings in {slide_path}")

            shape = dataset.shape
            dtype = dataset.dtype

            # Read in chunks to limit peak memory (64 MB per chunk)
            chunk_bytes = 64 * 1024 * 1024
            row_bytes = int(np.prod(shape[1:])) * np.dtype(dtype).itemsize if len(shape) > 1 else np.dtype(dtype).itemsize
            rows_per_chunk = max(1, chunk_bytes // max(row_bytes, 1))

            arr = np.empty(shape, dtype=np.float32)
            for start in range(0, shape[0], rows_per_chunk):
                end = min(start + rows_per_chunk, shape[0])
                arr[start:end] = dataset[start:end]

            # UNI2 format: (1, N, 1536) -> squeeze to (N, 1536)
            if arr.ndim == 3 and arr.shape[0] == 1:
                arr = arr.squeeze(0)
            return arr

    def discover_slides(self) -> List[Dict[str, Any]]:
        """Discover all available slide embeddings and match with clinical data."""
        logger.info(f"Discovering slides in {self.config.embeddings_dir}")

        slides = []
        h5_files = list(self.config.embeddings_dir.glob("**/*.h5"))
        h5_files.extend(list(self.config.embeddings_dir.glob("**/*.hdf5")))

        logger.info(f"Found {len(h5_files)} H5 files")

        for h5_path in h5_files:
            # Extract TCGA barcode from filename
            filename = h5_path.stem

            # Try to parse TCGA barcode (e.g., TCGA-W5-AA2R-01A-01-TS1)
            parts = filename.split('-')
            if len(parts) >= 4 and parts[0] == 'TCGA':
                participant_id = '-'.join(parts[:3])
                sample_type_code = parts[3][:2] if len(parts) > 3 else '01'

                # Determine if tumor or normal based on sample type code
                # 01-09 = tumor, 10-19 = normal (11 = solid tissue normal)
                try:
                    code = int(sample_type_code)
                    is_tumor = code < 10
                except ValueError:
                    is_tumor = True  # Default to tumor if can't parse

                slides.append({
                    'path': h5_path,
                    'filename': filename,
                    'participant_id': participant_id,
                    'sample_type_code': sample_type_code,
                    'is_tumor': is_tumor,
                    'label': 'tumor' if is_tumor else 'normal'
                })

        self.slide_metadata = pd.DataFrame(slides)
        logger.info(f"Discovered {len(slides)} slides")

        if len(slides) > 0:
            tumor_count = sum(1 for s in slides if s['is_tumor'])
            normal_count = len(slides) - tumor_count
            logger.info(f"  Tumor: {tumor_count}, Normal: {normal_count}")

        return slides

    def get_survival_data(self) -> pd.DataFrame:
        """Extract survival data from clinical data."""
        if self.clinical_data is None:
            self.load_clinical_data()

        clinical = self.clinical_data.copy()

        # Create survival outcome
        # Check for various column naming conventions
        time_cols = ['days_to_death', 'days_to_last_followup', 'days_to_last_follow_up',
                     'overall_survival', 'OS.time']
        status_cols = ['vital_status', 'OS', 'deceased']

        time_col = None
        status_col = None

        for col in time_cols:
            if col in clinical.columns:
                time_col = col
                break

        for col in status_cols:
            if col in clinical.columns:
                status_col = col
                break

        if time_col is None or status_col is None:
            logger.warning("Could not find survival columns in clinical data")
            return pd.DataFrame()

        # Create combined time column
        if 'days_to_death' in clinical.columns and 'days_to_last_followup' in clinical.columns:
            clinical['time'] = clinical['days_to_death'].fillna(clinical['days_to_last_followup'])
        else:
            clinical['time'] = clinical[time_col]

        # Create event indicator (1 = death, 0 = censored)
        if status_col == 'vital_status':
            clinical['event'] = (clinical[status_col].str.lower() == 'dead').astype(int)
        else:
            clinical['event'] = clinical[status_col].astype(int)

        # Remove samples with missing survival data
        survival_data = clinical[['participant_id', 'time', 'event']].dropna()
        survival_data = survival_data[survival_data['time'] > 0]

        logger.info(f"Survival data: {len(survival_data)} samples, {survival_data['event'].sum()} events")

        return survival_data


