class WSIAnalysisPipeline:
    """Main pipeline orchestrating the entire analysis."""

    def __init__(self, config: Optional[Config] = None):
        self.config = config or Config()
        self.data_loader = DataLoader(self.config)
        self.clustering = ClusteringPipeline(self.config)
        self.cv = CrossValidation(self.config)
        self.stats = StatisticalTesting(self.config)
        self.exporter = ResultsExporter(self.config)

        self.aggregation_methods = get_all_aggregation_methods()
        self.survival_models = get_survival_models(self.config.random_state)
        self.classification_models = get_classification_models(self.config.random_state)

    def run(self):
        """Execute the full analysis pipeline."""
        start_time = datetime.now()
        logger.info("=" * 60)
        logger.info("WSI Embedding Analysis Pipeline")
        logger.info(f"Started at: {start_time}")
        logger.info("=" * 60)

        # Step 1: Load data
        logger.info("\n[Step 1/6] Loading data...")
        slides = self.data_loader.discover_slides()

        if len(slides) == 0:
            logger.error("No slides found. Please check the embeddings directory.")
            logger.info(f"Expected location: {self.config.embeddings_dir}")
            self._create_demo_results()
            return

        clinical_data = self.data_loader.load_clinical_data()
        survival_data = self.data_loader.get_survival_data()

        # Step 2: Aggregate embeddings
        logger.info("\n[Step 2/6] Aggregating embeddings...")
        aggregated_embeddings = self._aggregate_all_slides(slides)

        # Get labels
        slide_df = self.data_loader.slide_metadata
        labels = slide_df['is_tumor'].astype(int).values
        slide_ids = slide_df['filename'].tolist()

        # Save aggregated embeddings
        self.exporter.save_aggregated_embeddings(aggregated_embeddings, slide_ids)

        # Step 3: Clustering analysis
        logger.info("\n[Step 3/6] Running clustering analysis...")
        clustering_results = {}
        slide_participant_ids = slide_df['participant_id'].tolist()
        for method_name, embeddings in aggregated_embeddings.items():
            logger.info(f"  Processing {method_name}...")
            results = self.clustering.run_clustering_analysis(
                embeddings, method_name, labels,
                survival_data if len(survival_data) > 0 else None,
                slide_participant_ids=slide_participant_ids
            )
            clustering_results[method_name] = results

        self.exporter.save_clustering_results(clustering_results)

        # Step 4: Classification analysis
        logger.info("\n[Step 4/6] Running classification analysis...")
        classification_results = self._run_classification_analysis(aggregated_embeddings, labels)
        class_df = self.exporter.save_classification_results(classification_results)

        # Step 5: Survival analysis
        logger.info("\n[Step 5/6] Running survival analysis...")
        if len(survival_data) > 0:
            survival_results = self._run_survival_analysis(aggregated_embeddings, survival_data, slide_df)
            surv_df = self.exporter.save_survival_results(survival_results)
        else:
            logger.warning("No survival data available. Skipping survival analysis.")
            surv_df = pd.DataFrame()

        # Step 6: Statistical testing and summary
        logger.info("\n[Step 6/6] Compiling results and statistical tests...")
        stat_df = self.stats.compile_statistical_results(clustering_results)
        self.exporter.save_statistical_tests(stat_df)

        # Create visualizations
        if self.clustering.pca_models:
            self.exporter.plot_pca_variance(self.clustering.pca_models)
        if len(class_df) > 0:
            self.exporter.plot_classification_heatmap(class_df)
        if len(surv_df) > 0:
            self.exporter.plot_survival_heatmap(surv_df)

        # Create summary report
        summary = self.exporter.create_summary_report(
            class_df, surv_df, clustering_results, stat_df
        )

        # Final summary
        end_time = datetime.now()
        duration = end_time - start_time

        logger.info("\n" + "=" * 60)
        logger.info("ANALYSIS COMPLETE")
        logger.info("=" * 60)
        logger.info(f"Duration: {duration}")
        logger.info(f"Results saved to: {self.config.output_dir}")
        logger.info(f"\nSummary:")
        logger.info(f"  - Aggregation methods: {len(self.aggregation_methods)}")
        logger.info(f"  - Classification models: {len(self.classification_models)}")
        logger.info(f"  - Survival models: {len(self.survival_models)}")
        logger.info(f"  - Total classification evaluations: {len(classification_results)}")
        logger.info(f"  - Total survival evaluations: {len(survival_results) if 'survival_results' in dir() else 0}")

        if len(stat_df) > 0:
            n_significant = stat_df['significant_fdr'].sum() if 'significant_fdr' in stat_df.columns else 0
            logger.info(f"  - Significant tests (FDR < 0.05): {n_significant}/{len(stat_df)}")

        return summary

    def _aggregate_all_slides(self, slides: List[Dict]) -> Dict[str, np.ndarray]:
        """Aggregate embeddings for all slides using all methods.

        Processes one file at a time with immediate memory cleanup and
        cooldown pauses between files to stay thermally friendly.
        """
        aggregated = {method: [] for method in self.aggregation_methods}
        total = len(slides)

        for idx, slide_info in enumerate(slides):
            slide_path = slide_info['path']
            logger.info(f"  [{idx + 1}/{total}] Processing {slide_path.name}")

            try:
                # Load embeddings for this single slide
                embeddings = self.data_loader.load_slide_embeddings(slide_path)

                # Apply all aggregation methods
                for method_name, method_func in self.aggregation_methods.items():
                    try:
                        agg_emb = method_func(embeddings)
                        aggregated[method_name].append(agg_emb)
                    except Exception as e:
                        logger.warning(f"Aggregation {method_name} failed for {slide_path.name}: {e}")
                        expected_dim = self._get_expected_dim(method_name)
                        aggregated[method_name].append(np.zeros(expected_dim))

            except Exception as e:
                logger.error(f"Failed to load {slide_path}: {e}")
                for method_name in self.aggregation_methods:
                    expected_dim = self._get_expected_dim(method_name)
                    aggregated[method_name].append(np.zeros(expected_dim))

            # --- Immediate memory cleanup after each file ---
            # Delete the raw embeddings so RAM is freed before the next file
            try:
                del embeddings
            except UnboundLocalError:
                pass
            gc.collect()

            # --- Cooldown pause between files ---
            if idx < total - 1:
                time.sleep(self.config.cooldown_seconds)

        # Convert to arrays
        for method_name in aggregated:
            aggregated[method_name] = np.array(aggregated[method_name])
            logger.info(f"  {method_name}: {aggregated[method_name].shape}")

        return aggregated

    def _get_expected_dim(self, method_name: str) -> int:
        """Get expected output dimension for an aggregation method."""
        mega_dims = {
            'mega_mean_minmax': 4608,
            'mega_mean_extremes': 7680,
            'mega_mean_percentiles': 7680,
            'mega_central_spread': 7680,
            'mega_robust': 7680,
            'mega_full': 10752,
            'mega_distribution': 6144,
            'mega_all_percentiles': 10752,
        }
        return mega_dims.get(method_name, self.config.embedding_dim)

    def _run_classification_analysis(self, aggregated_embeddings: Dict[str, np.ndarray],
                                     labels: np.ndarray) -> List[Dict]:
        """Run classification analysis for all aggregation methods and models."""
        results = []

        total_combos = len(aggregated_embeddings) * len(self.classification_models)
        current = 0

        for method_name, embeddings in aggregated_embeddings.items():
            # PCA first
            embeddings_pca, _ = self.clustering.fit_pca(embeddings, f"class_{method_name}")

            for model_name, model in self.classification_models.items():
                current += 1
                logger.info(f"  [{current}/{total_combos}] {method_name} + {model_name}")

                try:
                    # 5-fold CV
                    cv_result = self.cv.stratified_kfold_classification(
                        embeddings_pca, labels, model, model_name
                    )
                    cv_result['aggregation_method'] = method_name
                    results.append(cv_result)

                except Exception as e:
                    logger.warning(f"Classification failed for {method_name} + {model_name}: {e}")
                    results.append({
                        'aggregation_method': method_name,
                        'model_name': model_name,
                        'cv_type': f'{self.config.n_folds}-fold',
                        'auc_mean': np.nan,
                        'auc_std': np.nan,
                        'f1_mean': np.nan,
                        'f1_std': np.nan,
                        'accuracy_mean': np.nan,
                        'accuracy_std': np.nan,
                        'error': str(e)
                    })

        return results

    def _run_survival_analysis(self, aggregated_embeddings: Dict[str, np.ndarray],
                               survival_data: pd.DataFrame,
                               slide_df: pd.DataFrame) -> List[Dict]:
        """Run survival analysis for all aggregation methods and models."""
        results = []

        # Match survival data with slide embeddings
        # Create participant to index mapping
        participant_to_idx = {}
        for idx, row in slide_df.iterrows():
            participant_to_idx[row['participant_id']] = idx

        # Find common samples
        common_participants = set(survival_data['participant_id']).intersection(
            set(slide_df['participant_id'])
        )

        if len(common_participants) == 0:
            logger.warning("No matching samples between embeddings and survival data")
            return results

        logger.info(f"Found {len(common_participants)} samples with both embeddings and survival data")

        # Create aligned indices
        survival_indices = []
        embedding_indices = []

        for participant in common_participants:
            surv_idx = survival_data[survival_data['participant_id'] == participant].index[0]
            emb_idx = slide_df[slide_df['participant_id'] == participant].index[0]
            survival_indices.append(surv_idx)
            embedding_indices.append(emb_idx)

        # Create structured array for survival
        aligned_survival = survival_data.loc[survival_indices]
        y = np.array(
            [(bool(row['event']), row['time']) for _, row in aligned_survival.iterrows()],
            dtype=[('event', bool), ('time', float)]
        )

        total_combos = len(aggregated_embeddings) * len(self.survival_models)
        current = 0

        for method_name, embeddings in aggregated_embeddings.items():
            # Get aligned embeddings
            X = embeddings[embedding_indices]

            # PCA
            embeddings_pca, _ = self.clustering.fit_pca(X, f"surv_{method_name}")

            for model_name, model in self.survival_models.items():
                current += 1
                logger.info(f"  [{current}/{total_combos}] {method_name} + {model_name}")

                try:
                    cv_result = self.cv.stratified_kfold_survival(
                        embeddings_pca, y, model, model_name
                    )
                    cv_result['aggregation_method'] = method_name
                    results.append(cv_result)

                except Exception as e:
                    logger.warning(f"Survival failed for {method_name} + {model_name}: {e}")
                    results.append({
                        'aggregation_method': method_name,
                        'model_name': model_name,
                        'cv_type': f'{self.config.n_folds}-fold',
                        'c_index_mean': np.nan,
                        'c_index_std': np.nan,
                        'error': str(e)
                    })

        return results

    def _create_demo_results(self):
        """Create demo results when no real data is available."""
        logger.info("\nCreating demo results structure...")

        # Create empty summary
        summary = {
            'status': 'no_data',
            'message': 'No embedding files found. Pipeline structure verified.',
            'expected_location': str(self.config.embeddings_dir),
            'aggregation_methods': list(self.aggregation_methods.keys()),
            'classification_models': list(self.classification_models.keys()),
            'survival_models': list(self.survival_models.keys())
        }

        with open(self.config.output_dir / "demo_summary.json", 'w') as f:
            json.dump(summary, f, indent=2)

        logger.info(f"Demo summary saved to {self.config.output_dir / 'demo_summary.json'}")


