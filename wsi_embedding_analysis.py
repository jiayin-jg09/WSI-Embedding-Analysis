class CrossValidation:
    """Cross-validation utilities for survival and classification."""

    def __init__(self, config: Config):
        self.config = config

    def stratified_kfold_classification(self, X: np.ndarray, y: np.ndarray,
                                        model: Any, model_name: str) -> Dict:
        """Run stratified 5-fold CV for classification."""
        skf = StratifiedKFold(n_splits=self.config.n_folds, shuffle=True,
                             random_state=self.config.random_state)

        metrics = {
            'auc': [], 'f1': [], 'accuracy': [],
            'precision': [], 'recall': []
        }

        for fold, (train_idx, test_idx) in enumerate(skf.split(X, y)):
            X_train, X_test = X[train_idx], X[test_idx]
            y_train, y_test = y[train_idx], y[test_idx]

            try:
                # Fit model
                model_copy = self._clone_model(model)
                model_copy.fit(X_train, y_train)

                # Predictions
                y_pred = model_copy.predict(X_test)

                # Probabilities for AUC
                if hasattr(model_copy, 'predict_proba'):
                    y_prob = model_copy.predict_proba(X_test)[:, 1]
                elif hasattr(model_copy, 'decision_function'):
                    y_prob = model_copy.decision_function(X_test)
                else:
                    y_prob = y_pred

                # Calculate metrics
                metrics['accuracy'].append(accuracy_score(y_test, y_pred))
                metrics['f1'].append(f1_score(y_test, y_pred, zero_division=0))
                metrics['precision'].append(precision_score(y_test, y_pred, zero_division=0))
                metrics['recall'].append(recall_score(y_test, y_pred, zero_division=0))

                try:
                    metrics['auc'].append(roc_auc_score(y_test, y_prob))
                except:
                    metrics['auc'].append(np.nan)

            except Exception as e:
                logger.warning(f"Fold {fold} failed for {model_name}: {e}")
                for key in metrics:
                    metrics[key].append(np.nan)

        # Aggregate results
        results = {
            'model_name': model_name,
            'cv_type': f'{self.config.n_folds}-fold'
        }
        for key in metrics:
            results[f'{key}_mean'] = np.nanmean(metrics[key])
            results[f'{key}_std'] = np.nanstd(metrics[key])

        return results

    def loo_classification(self, X: np.ndarray, y: np.ndarray,
                          model: Any, model_name: str) -> Dict:
        """Run Leave-One-Out CV for classification."""
        loo = LeaveOneOut()

        y_true = []
        y_pred = []
        y_prob = []

        for train_idx, test_idx in loo.split(X):
            X_train, X_test = X[train_idx], X[test_idx]
            y_train, y_test = y[train_idx], y[test_idx]

            try:
                model_copy = self._clone_model(model)
                model_copy.fit(X_train, y_train)

                pred = model_copy.predict(X_test)[0]
                y_true.append(y_test[0])
                y_pred.append(pred)

                if hasattr(model_copy, 'predict_proba'):
                    prob = model_copy.predict_proba(X_test)[0, 1]
                elif hasattr(model_copy, 'decision_function'):
                    prob = model_copy.decision_function(X_test)[0]
                else:
                    prob = pred
                y_prob.append(prob)

            except Exception as e:
                y_true.append(y_test[0])
                y_pred.append(np.nan)
                y_prob.append(np.nan)

        y_true = np.array(y_true)
        y_pred = np.array(y_pred)
        y_prob = np.array(y_prob)

        # Remove NaN entries
        valid_mask = ~np.isnan(y_pred)
        y_true_valid = y_true[valid_mask]
        y_pred_valid = y_pred[valid_mask]
        y_prob_valid = y_prob[valid_mask]

        results = {
            'model_name': model_name,
            'cv_type': 'LOO',
            'accuracy': accuracy_score(y_true_valid, y_pred_valid) if len(y_true_valid) > 0 else np.nan,
            'f1': f1_score(y_true_valid, y_pred_valid, zero_division=0) if len(y_true_valid) > 0 else np.nan,
            'precision': precision_score(y_true_valid, y_pred_valid, zero_division=0) if len(y_true_valid) > 0 else np.nan,
            'recall': recall_score(y_true_valid, y_pred_valid, zero_division=0) if len(y_true_valid) > 0 else np.nan,
        }

        try:
            results['auc'] = roc_auc_score(y_true_valid, y_prob_valid)
        except:
            results['auc'] = np.nan

        return results

    def stratified_kfold_survival(self, X: np.ndarray, y: np.ndarray,
                                  model: Any, model_name: str) -> Dict:
        """Run stratified 5-fold CV for survival analysis."""
        # y is structured array with (event, time)
        events = y['event']

        skf = StratifiedKFold(n_splits=self.config.n_folds, shuffle=True,
                             random_state=self.config.random_state)

        c_indices = []

        for fold, (train_idx, test_idx) in enumerate(skf.split(X, events)):
            X_train, X_test = X[train_idx], X[test_idx]
            y_train, y_test = y[train_idx], y[test_idx]

            try:
                if isinstance(model, str) and model.startswith('lifelines_'):
                    c_index = self._fit_lifelines_model(model, X_train, y_train, X_test, y_test)
                else:
                    model_copy = self._clone_model(model)
                    model_copy.fit(X_train, y_train)

                    # Get risk scores
                    if hasattr(model_copy, 'predict'):
                        risk_scores = model_copy.predict(X_test)
                    else:
                        risk_scores = model_copy.decision_function(X_test)

                    # Calculate C-index
                    c_index = concordance_index_censored(
                        y_test['event'], y_test['time'], risk_scores
                    )[0]

                c_indices.append(c_index)

            except Exception as e:
                logger.warning(f"Fold {fold} failed for {model_name}: {e}")
                c_indices.append(np.nan)

        results = {
            'model_name': model_name,
            'cv_type': f'{self.config.n_folds}-fold',
            'c_index_mean': np.nanmean(c_indices),
            'c_index_std': np.nanstd(c_indices)
        }

        return results

    def _fit_lifelines_model(self, model_type: str, X_train: np.ndarray, y_train: np.ndarray,
                            X_test: np.ndarray, y_test: np.ndarray) -> float:
        """Fit lifelines survival model and return C-index."""
        # Create DataFrame for lifelines
        train_df = pd.DataFrame(X_train, columns=[f'feature_{i}' for i in range(X_train.shape[1])])
        train_df['time'] = y_train['time']
        train_df['event'] = y_train['event']

        test_df = pd.DataFrame(X_test, columns=[f'feature_{i}' for i in range(X_test.shape[1])])
        test_df['time'] = y_test['time']
        test_df['event'] = y_test['event']

        feature_cols = [f'feature_{i}' for i in range(X_train.shape[1])]

        if model_type == 'lifelines_cox':
            fitter = CoxPHFitter(penalizer=0.1)
            fitter.fit(train_df, duration_col='time', event_col='event')
            partial_hazard = fitter.predict_partial_hazard(test_df)
            risk_scores = partial_hazard.values.flatten()
        elif model_type == 'lifelines_weibull':
            fitter = WeibullAFTFitter(penalizer=0.1)
            fitter.fit(train_df, duration_col='time', event_col='event')
            # For AFT, lower predicted time = higher risk
            risk_scores = -fitter.predict_expectation(test_df).values.flatten()
        elif model_type == 'lifelines_lognormal':
            fitter = LogNormalAFTFitter(penalizer=0.1)
            fitter.fit(train_df, duration_col='time', event_col='event')
            risk_scores = -fitter.predict_expectation(test_df).values.flatten()
        elif model_type == 'lifelines_loglogistic':
            fitter = LogLogisticAFTFitter(penalizer=0.1)
            fitter.fit(train_df, duration_col='time', event_col='event')
            risk_scores = -fitter.predict_expectation(test_df).values.flatten()
        else:
            raise ValueError(f"Unknown lifelines model: {model_type}")

        c_index = concordance_index_censored(
            y_test['event'], y_test['time'], risk_scores
        )[0]

        return c_index

    def _clone_model(self, model: Any) -> Any:
        """Clone a sklearn-style model."""
        from sklearn.base import clone
        try:
            return clone(model)
        except:
            # For models that don't support clone, create new instance
            return model.__class__(**model.get_params())


