class AggregationMethods:
    """Collection of 43 aggregation methods for patch embeddings."""

    @staticmethod
    def mean(embeddings: np.ndarray) -> np.ndarray:
        """Simple arithmetic mean."""
        return np.mean(embeddings, axis=0)

    @staticmethod
    def median(embeddings: np.ndarray) -> np.ndarray:
        """Median along patches."""
        return np.median(embeddings, axis=0)

    @staticmethod
    def trimmed_mean(embeddings: np.ndarray, proportion: float = 0.1) -> np.ndarray:
        """Trimmed mean excluding top/bottom 10%."""
        return stats.trim_mean(embeddings, proportion, axis=0)

    @staticmethod
    def winsorized_mean(embeddings: np.ndarray, limits: Tuple[float, float] = (0.1, 0.1)) -> np.ndarray:
        """Winsorized mean (cap extremes instead of remove)."""
        from scipy.stats import mstats
        return np.mean(mstats.winsorize(embeddings, limits=limits, axis=0), axis=0)

    @staticmethod
    def geometric_mean(embeddings: np.ndarray) -> np.ndarray:
        """Geometric mean (after shifting to positive values)."""
        # Shift to positive domain
        shifted = embeddings - embeddings.min(axis=0) + 1e-10
        return stats.gmean(shifted, axis=0) + embeddings.min(axis=0) - 1e-10

    @staticmethod
    def harmonic_mean(embeddings: np.ndarray) -> np.ndarray:
        """Harmonic mean (after shifting to positive values)."""
        shifted = embeddings - embeddings.min(axis=0) + 1e-10
        return stats.hmean(shifted, axis=0) + embeddings.min(axis=0) - 1e-10

    @staticmethod
    def std(embeddings: np.ndarray) -> np.ndarray:
        """Standard deviation."""
        return np.std(embeddings, axis=0)

    @staticmethod
    def var(embeddings: np.ndarray) -> np.ndarray:
        """Variance."""
        return np.var(embeddings, axis=0)

    @staticmethod
    def range_agg(embeddings: np.ndarray) -> np.ndarray:
        """Range (max - min)."""
        return np.ptp(embeddings, axis=0)

    @staticmethod
    def cv(embeddings: np.ndarray) -> np.ndarray:
        """Coefficient of variation (std/mean)."""
        mean_vals = np.mean(embeddings, axis=0)
        std_vals = np.std(embeddings, axis=0)
        # Avoid division by zero
        return np.divide(std_vals, mean_vals, out=np.zeros_like(std_vals), where=mean_vals != 0)

    @staticmethod
    def iqr(embeddings: np.ndarray) -> np.ndarray:
        """Interquartile range."""
        return stats.iqr(embeddings, axis=0)

    @staticmethod
    def mad(embeddings: np.ndarray) -> np.ndarray:
        """Median absolute deviation."""
        return stats.median_abs_deviation(embeddings, axis=0)

    @staticmethod
    def mid_range(embeddings: np.ndarray) -> np.ndarray:
        """Mid-range ((max + min) / 2)."""
        return (np.max(embeddings, axis=0) + np.min(embeddings, axis=0)) / 2

    @staticmethod
    def entropy_agg(embeddings: np.ndarray, bins: int = 50) -> np.ndarray:
        """Shannon entropy of discretized values per dimension."""
        result = np.zeros(embeddings.shape[1])
        for i in range(embeddings.shape[1]):
            hist, _ = np.histogram(embeddings[:, i], bins=bins, density=True)
            hist = hist + 1e-10  # Avoid log(0)
            result[i] = scipy_entropy(hist)
        return result

    @staticmethod
    def skewness(embeddings: np.ndarray) -> np.ndarray:
        """Skewness of distribution."""
        return stats.skew(embeddings, axis=0)

    @staticmethod
    def kurtosis(embeddings: np.ndarray) -> np.ndarray:
        """Kurtosis of distribution."""
        return stats.kurtosis(embeddings, axis=0)

    @staticmethod
    def percentile_5(embeddings: np.ndarray) -> np.ndarray:
        """5th percentile."""
        return np.percentile(embeddings, 5, axis=0)

    @staticmethod
    def percentile_10(embeddings: np.ndarray) -> np.ndarray:
        """10th percentile."""
        return np.percentile(embeddings, 10, axis=0)

    @staticmethod
    def percentile_25(embeddings: np.ndarray) -> np.ndarray:
        """25th percentile (Q1)."""
        return np.percentile(embeddings, 25, axis=0)

    @staticmethod
    def percentile_50(embeddings: np.ndarray) -> np.ndarray:
        """50th percentile (median)."""
        return np.percentile(embeddings, 50, axis=0)

    @staticmethod
    def percentile_75(embeddings: np.ndarray) -> np.ndarray:
        """75th percentile (Q3)."""
        return np.percentile(embeddings, 75, axis=0)

    @staticmethod
    def percentile_90(embeddings: np.ndarray) -> np.ndarray:
        """90th percentile."""
        return np.percentile(embeddings, 90, axis=0)

    @staticmethod
    def percentile_95(embeddings: np.ndarray) -> np.ndarray:
        """95th percentile."""
        return np.percentile(embeddings, 95, axis=0)

    @staticmethod
    def max_agg(embeddings: np.ndarray) -> np.ndarray:
        """Maximum value."""
        return np.max(embeddings, axis=0)

    @staticmethod
    def min_agg(embeddings: np.ndarray) -> np.ndarray:
        """Minimum value."""
        return np.min(embeddings, axis=0)

    @staticmethod
    def top5_mean(embeddings: np.ndarray) -> np.ndarray:
        """Mean of top 5% patches by L2 norm."""
        norms = np.linalg.norm(embeddings, axis=1)
        k = max(1, int(len(embeddings) * 0.05))
        top_indices = np.argsort(norms)[-k:]
        return np.mean(embeddings[top_indices], axis=0)

    @staticmethod
    def top10_mean(embeddings: np.ndarray) -> np.ndarray:
        """Mean of top 10% patches by L2 norm."""
        norms = np.linalg.norm(embeddings, axis=1)
        k = max(1, int(len(embeddings) * 0.10))
        top_indices = np.argsort(norms)[-k:]
        return np.mean(embeddings[top_indices], axis=0)

    @staticmethod
    def bottom5_mean(embeddings: np.ndarray) -> np.ndarray:
        """Mean of bottom 5% patches by L2 norm."""
        norms = np.linalg.norm(embeddings, axis=1)
        k = max(1, int(len(embeddings) * 0.05))
        bottom_indices = np.argsort(norms)[:k]
        return np.mean(embeddings[bottom_indices], axis=0)

    @staticmethod
    def bottom10_mean(embeddings: np.ndarray) -> np.ndarray:
        """Mean of bottom 10% patches by L2 norm."""
        norms = np.linalg.norm(embeddings, axis=1)
        k = max(1, int(len(embeddings) * 0.10))
        bottom_indices = np.argsort(norms)[:k]
        return np.mean(embeddings[bottom_indices], axis=0)

    @staticmethod
    def top5_mean_L1(embeddings: np.ndarray) -> np.ndarray:
        """Mean of top 5% patches by L1 norm."""
        norms = np.linalg.norm(embeddings, ord=1, axis=1)
        k = max(1, int(len(embeddings) * 0.05))
        top_indices = np.argsort(norms)[-k:]
        return np.mean(embeddings[top_indices], axis=0)

    @staticmethod
    def median_patch(embeddings: np.ndarray) -> np.ndarray:
        """Single patch closest to median embedding."""
        median_emb = np.median(embeddings, axis=0)
        distances = np.linalg.norm(embeddings - median_emb, axis=1)
        return embeddings[np.argmin(distances)]

    @staticmethod
    def entropy_weighted_mean(embeddings: np.ndarray, bins: int = 50) -> np.ndarray:
        """Mean weighted by inverse entropy (higher weight to consistent patches)."""
        # Compute entropy per patch
        patch_entropies = np.zeros(len(embeddings))
        for i, patch in enumerate(embeddings):
            hist, _ = np.histogram(patch, bins=bins, density=True)
            hist = hist + 1e-10
            patch_entropies[i] = scipy_entropy(hist)

        # Inverse entropy as weight (low entropy = high weight)
        weights = 1.0 / (patch_entropies + 1e-10)
        weights = weights / weights.sum()
        return np.average(embeddings, axis=0, weights=weights)

    @staticmethod
    def variance_weighted_mean(embeddings: np.ndarray) -> np.ndarray:
        """Mean weighted by inverse variance (higher weight to consistent patches)."""
        patch_vars = np.var(embeddings, axis=1)
        weights = 1.0 / (patch_vars + 1e-10)
        weights = weights / weights.sum()
        return np.average(embeddings, axis=0, weights=weights)

    @staticmethod
    def rms(embeddings: np.ndarray) -> np.ndarray:
        """Root mean square."""
        return np.sqrt(np.mean(embeddings ** 2, axis=0))

    @staticmethod
    def sum_abs(embeddings: np.ndarray) -> np.ndarray:
        """Sum of absolute values per dimension."""
        return np.sum(np.abs(embeddings), axis=0)

    # Mega-vectors (concatenations)
    @staticmethod
    def mega_mean_minmax(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + min + max (4,608 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.min_agg(embeddings),
            AggregationMethods.max_agg(embeddings)
        ])

    @staticmethod
    def mega_mean_extremes(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + top5 + bottom5 + top10 + bottom10 (7,680 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.top5_mean(embeddings),
            AggregationMethods.bottom5_mean(embeddings),
            AggregationMethods.top10_mean(embeddings),
            AggregationMethods.bottom10_mean(embeddings)
        ])

    @staticmethod
    def mega_mean_percentiles(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + P10 + P25 + P75 + P90 (7,680 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.percentile_10(embeddings),
            AggregationMethods.percentile_25(embeddings),
            AggregationMethods.percentile_75(embeddings),
            AggregationMethods.percentile_90(embeddings)
        ])

    @staticmethod
    def mega_central_spread(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + std + var + range + iqr (7,680 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.std(embeddings),
            AggregationMethods.var(embeddings),
            AggregationMethods.range_agg(embeddings),
            AggregationMethods.iqr(embeddings)
        ])

    @staticmethod
    def mega_robust(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: median + trimmed_mean + iqr + P25 + P75 (7,680 dims)."""
        return np.concatenate([
            AggregationMethods.median(embeddings),
            AggregationMethods.trimmed_mean(embeddings),
            AggregationMethods.iqr(embeddings),
            AggregationMethods.percentile_25(embeddings),
            AggregationMethods.percentile_75(embeddings)
        ])

    @staticmethod
    def mega_full(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + min + max + top5 + bottom5 + std + range (10,752 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.min_agg(embeddings),
            AggregationMethods.max_agg(embeddings),
            AggregationMethods.top5_mean(embeddings),
            AggregationMethods.bottom5_mean(embeddings),
            AggregationMethods.std(embeddings),
            AggregationMethods.range_agg(embeddings)
        ])

    @staticmethod
    def mega_distribution(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: mean + std + skewness + kurtosis (6,144 dims)."""
        return np.concatenate([
            AggregationMethods.mean(embeddings),
            AggregationMethods.std(embeddings),
            AggregationMethods.skewness(embeddings),
            AggregationMethods.kurtosis(embeddings)
        ])

    @staticmethod
    def mega_all_percentiles(embeddings: np.ndarray) -> np.ndarray:
        """Mega-vector: P5 + P10 + P25 + P50 + P75 + P90 + P95 (10,752 dims)."""
