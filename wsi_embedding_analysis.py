class ClusteringPipeline:
    """PCA and clustering analysis pipeline."""

    def __init__(self, config: Config):
        self.config = config
        self.pca_models = {}
        self.cluster_results = {}

    def fit_pca(self, embeddings: np.ndarray, method_name: str) -> Tuple[np.ndarray, PCA]:
        """Fit PCA with 95% variance threshold."""
        logger.info(f"Fitting PCA for {method_name} (input dim: {embeddings.shape[1]})")

        # Standardize
        scaler = StandardScaler()
        embeddings_scaled = scaler.fit_transform(embeddings)

        # Fit PCA with variance threshold
        pca = PCA(n_components=self.config.pca_variance_threshold, svd_solver='full')
        embeddings_pca = pca.fit_transform(embeddings_scaled)

        logger.info(f"  PCA reduced to {pca.n_components_} components "
                   f"({pca.explained_variance_ratio_.sum():.2%} variance)")

        self.pca_models[method_name] = {
            'pca': pca,
            'scaler': scaler,
            'n_components': pca.n_components_,
            'explained_variance': pca.explained_variance_ratio_.sum()
        }

        return embeddings_pca, pca

    def find_optimal_k(self, embeddings: np.ndarray, method: str = 'silhouette') -> int:
        """Find optimal number of clusters using silhouette score."""
        best_k = 2
        best_score = -1

        scores = {}
        for k in range(self.config.min_clusters, min(self.config.max_clusters + 1, len(embeddings))):
            kmeans = KMeans(n_clusters=k, random_state=self.config.random_state, n_init=10)
            labels = kmeans.fit_predict(embeddings)

            if method == 'silhouette':
                score = silhouette_score(embeddings, labels)
            elif method == 'calinski':
                score = calinski_harabasz_score(embeddings, labels)
            else:
                score = silhouette_score(embeddings, labels)

            scores[k] = score
            if score > best_score:
                best_score = score
                best_k = k

        return best_k, scores

    def cluster_kmeans(self, embeddings: np.ndarray, k: int) -> np.ndarray:
        """Perform K-means clustering."""
        kmeans = KMeans(n_clusters=k, random_state=self.config.random_state, n_init=10)
        return kmeans.fit_predict(embeddings)

    def cluster_hierarchical(self, embeddings: np.ndarray, k: int) -> np.ndarray:
        """Perform hierarchical (Ward) clustering."""
        clustering = AgglomerativeClustering(n_clusters=k, linkage='ward')
        return clustering.fit_predict(embeddings)

    def run_clustering_analysis(self, embeddings: np.ndarray, method_name: str,
                                labels: np.ndarray, survival_data: Optional[pd.DataFrame] = None,
                                slide_participant_ids: Optional[List[str]] = None) -> Dict:
        """Run full clustering analysis for one aggregation method."""
        results = {
            'method_name': method_name,
            'n_samples': len(embeddings)
        }

        # PCA
        embeddings_pca, pca = self.fit_pca(embeddings, method_name)
        results['pca_n_components'] = pca.n_components_
        results['pca_variance_explained'] = pca.explained_variance_ratio_.sum()

        # Find optimal k
        optimal_k, silhouette_scores = self.find_optimal_k(embeddings_pca)
        results['optimal_k'] = optimal_k
        results['silhouette_scores'] = silhouette_scores

        # K-means clustering
        kmeans_labels = self.cluster_kmeans(embeddings_pca, optimal_k)
        results['kmeans_labels'] = kmeans_labels
        results['kmeans_silhouette'] = silhouette_score(embeddings_pca, kmeans_labels)

        # Hierarchical clustering
        hier_labels = self.cluster_hierarchical(embeddings_pca, optimal_k)
        results['hierarchical_labels'] = hier_labels
        results['hierarchical_silhouette'] = silhouette_score(embeddings_pca, hier_labels)

        # Statistical tests
        # Chi-square: clusters vs tumor/normal
        if labels is not None and len(np.unique(labels)) > 1:
            for cluster_type, cluster_labels in [('kmeans', kmeans_labels), ('hierarchical', hier_labels)]:
                contingency = pd.crosstab(cluster_labels, labels)
                chi2, pval, dof, expected = stats.chi2_contingency(contingency)
                results[f'{cluster_type}_chi2_stat'] = chi2
                results[f'{cluster_type}_chi2_pval'] = pval

        # Log-rank test: clusters vs survival
        # Align cluster labels with survival data via participant_id
        if (survival_data is not None and len(survival_data) > 0
                and LIFELINES_AVAILABLE and slide_participant_ids is not None):
            # Build mapping: participant_id -> cluster label
            surv_pids = set(survival_data['participant_id'].values)
            surv_indices = []
            cluster_indices = []
            for i, pid in enumerate(slide_participant_ids):
                if pid in surv_pids:
                    surv_indices.append(pid)
                    cluster_indices.append(i)

            if len(cluster_indices) >= 4:  # Need at least a few samples per cluster
                surv_subset = survival_data[survival_data['participant_id'].isin(surv_indices)].copy()
                # Map participant_id to cluster label
                pid_to_idx = {pid: i for i, pid in enumerate(slide_participant_ids)}

                for cluster_type, all_cluster_labels in [('kmeans', kmeans_labels), ('hierarchical', hier_labels)]:
                    try:
                        # Get cluster labels aligned with survival data
                        aligned_labels = np.array([
                            all_cluster_labels[pid_to_idx[pid]]
                            for pid in surv_subset['participant_id'].values
                        ])
                        unique_clusters = np.unique(aligned_labels)
                        if len(unique_clusters) >= 2:
                            mask1 = aligned_labels == unique_clusters[0]
                            mask2 = aligned_labels == unique_clusters[1]

                            lr_result = logrank_test(
                                surv_subset.loc[surv_subset.index[mask1], 'time'].values,
                                surv_subset.loc[surv_subset.index[mask2], 'time'].values,
                                surv_subset.loc[surv_subset.index[mask1], 'event'].values,
                                surv_subset.loc[surv_subset.index[mask2], 'event'].values
                            )
                            results[f'{cluster_type}_logrank_stat'] = lr_result.test_statistic
                            results[f'{cluster_type}_logrank_pval'] = lr_result.p_value
                    except Exception as e:
                        logger.warning(f"Log-rank test failed for {method_name} {cluster_type}: {e}")

        self.cluster_results[method_name] = results
        return results


