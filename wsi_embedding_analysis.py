class StatisticalTesting:
    """Statistical testing with FDR correction."""

    def __init__(self, config: Config):
        self.config = config

    def apply_fdr_correction(self, pvalues: np.ndarray,
                            method: str = 'fdr_bh') -> Tuple[np.ndarray, np.ndarray]:
        """Apply Benjamini-Hochberg FDR correction."""
        # Remove NaN values for correction
        valid_mask = ~np.isnan(pvalues)
        valid_pvals = pvalues[valid_mask]

        if len(valid_pvals) == 0:
            return pvalues, np.ones_like(pvalues, dtype=bool)

        # Apply correction
        rejected, corrected_pvals, _, _ = multipletests(
            valid_pvals, alpha=self.config.fdr_alpha, method=method
        )

        # Put back into original array
        corrected_full = np.full_like(pvalues, np.nan)
        rejected_full = np.zeros_like(pvalues, dtype=bool)

        corrected_full[valid_mask] = corrected_pvals
        rejected_full[valid_mask] = rejected

        return corrected_full, rejected_full

    def compile_statistical_results(self, clustering_results: Dict) -> pd.DataFrame:
        """Compile all statistical test results and apply FDR correction."""
        results = []

        for method_name, method_results in clustering_results.items():
            # Chi-square tests
            for cluster_type in ['kmeans', 'hierarchical']:
                stat_key = f'{cluster_type}_chi2_stat'
                pval_key = f'{cluster_type}_chi2_pval'

                if stat_key in method_results and pval_key in method_results:
                    results.append({
                        'aggregation_method': method_name,
                        'clustering_method': cluster_type,
                        'test_type': 'chi_square',
                        'test_statistic': method_results[stat_key],
                        'p_value': method_results[pval_key]
                    })

                # Log-rank tests
                stat_key = f'{cluster_type}_logrank_stat'
                pval_key = f'{cluster_type}_logrank_pval'

                if stat_key in method_results and pval_key in method_results:
                    results.append({
                        'aggregation_method': method_name,
                        'clustering_method': cluster_type,
                        'test_type': 'log_rank',
                        'test_statistic': method_results[stat_key],
                        'p_value': method_results[pval_key]
                    })

        if len(results) == 0:
            return pd.DataFrame()

        df = pd.DataFrame(results)

        # Apply FDR correction separately for each test type
        for test_type in df['test_type'].unique():
            mask = df['test_type'] == test_type
            pvals = df.loc[mask, 'p_value'].values
            corrected, rejected = self.apply_fdr_correction(pvals)
            df.loc[mask, 'p_value_fdr'] = corrected
            df.loc[mask, 'significant_fdr'] = rejected

        return df


